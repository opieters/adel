#returns stack of visible elements of the stem of an axe
stemElements <- function(desc) {
  metamer <- NULL
  elt <- NULL
  dl <- NULL
  for (i in seq(nrow(desc))) {
    if (desc$Ev[i] > 0) {
      metamer <- c(metamer,i)
      elt <- c(elt,"en")
      dl <- c(dl,desc$Ev[i])
    }
    if (desc$Gv[i] > 0) {
      metamer <- c(metamer,i)
      elt <- c(elt,"ga")
      dl <- c(dl,desc$Gv[i])
    }
  }
  data.frame(metamer=metamer,elt=elt,dl=dl)
}
